    void PopulateTerrainMap(){
        Noise.GenerateNoiseMap(width+1, width+1, 4, 0.3f, 2f, chunkPos, new Vector2(100,0), scale, 160f, 0.3f);
    	for (int x=0; x<width+1;x++){
    		for (int y=0; y<height+1;y++){
    			for (int z=0; z<width+1;z++){
    				float thisHeight = (float)(height - perlinScale)/2 + perlinScale * heightCurve.Evaluate(Noise.NoiseValue(x, z));

    				
    				float point = (float)y - thisHeight;

    				terrainMap[x,y,z] = point;
    			}
    		}
    	}
    }

    int GetCubeConfig(float[] cube){
    	int configIndex = 0;
    	for (int i=0; i<8; i++){
    		if (cube[i] > terrainSurface) configIndex |= 1 << i;
    	}

    	return configIndex;
    }

    void MarchCube(Vector3Int position){
        float[] cube = new float[8];
        for (int i=0; i<8; i++){
            Vector3Int corner = position + Vector3Int.FloorToInt(cornerTable[i]);
            cube[i] = terrainMap[corner.x, corner.y, corner.z];
        }

    	int configIndex = GetCubeConfig(cube);
    	if (configIndex == 0 || configIndex == 255) return;

    	int edgeIndex = 0;
    	for (int i=0; i<5; i++){ // max of 5 triangles to a cube
    		for (int p=0; p<3; p++){ // 3 vertices per triangle

    			int index = triangleTable[configIndex, edgeIndex];
    			if (index == -1) return;

    			Vector3 vert1 = position + edgeTable[index, 0];
    			Vector3 vert2 = position + edgeTable[index,  1];
    			Vector3 vertPosition = (vert1 + vert2) / 2f;
                vertPosition.x *= scale;
                vertPosition.z *= scale;

                colors.Add(gradient.Evaluate(((vertPosition.y+Random.Range(-1f,1f))-(height-perlinScale)/2)/perlinScale));
                //vertPosition.y = heightCurve.Evaluate((vertPosition.y-(height-perlinScale)/2)/perlinScale) * height;
    			vertices.Add(vertPosition);
    			triangles.Add(vertices.Count - 1);

                
    			edgeIndex++;
    		}
    	}
    }

    public void DestroyChunk(){
        vertices.Clear();
        triangles.Clear();
        colors.Clear();
        mesh.Clear();
    }

    public void BuildMesh(){
        DestroyChunk();
        for (int x=0; x<width;x++){
            for (int y=0; y<height;y++){
                for (int z=0; z<width;z++){

                    MarchCube(new Vector3Int(x,y,z));

                }
            }
        }
    	mesh.vertices = vertices.ToArray();
    	mesh.triangles = triangles.ToArray();
        mesh.colors = colors.ToArray();
    	mesh.RecalculateNormals();
    	collider.sharedMesh = mesh;
        filter.sharedMesh = mesh;
    }






